#include "libft.h"

char	*ft_strupcase(char *str)
{
	unsigned int c;

	c = 0;
	while (str[c] != '\0')
	{
		if (str[c] >= 'a' && str[c] <= 'z')
			str[c] = str[c] - 32;
		c++;
	}
	return (str);
}
